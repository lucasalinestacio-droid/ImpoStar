for (let category in EXTENSION_DATA_8) {
    if (DATA[category]) {
        DATA[category] = DATA[category].concat(EXTENSION_DATA_8[category]);
    } else {
        DATA[category] = EXTENSION_DATA_8[category];
    }
}

// FINAL DEDUPLICATION AND CLEANUP
for (let category in DATA) {
    const uniqueMap = new Map();
    DATA[category].forEach(item => {
        if (item && item.word) {
            // Normalize key for deduplication
            const key = item.word.toLowerCase().trim();
            // Preserve the one with more hints if duplicate
            if (!uniqueMap.has(key) || (item.hints && item.hints.length > uniqueMap.get(key).hints.length)) {
                uniqueMap.set(key, item);
            }
        }
    });
    // Convert back to array
    DATA[category] = Array.from(uniqueMap.values());
}

